What's the major difference between an Array and a Hash?

An array is an object that can store data like an ordered list of items, where each 
variable is accessible through its index.

A hash is an object that also allows you to store data, but instead of being a list it
consists of key/value pairs, allowing you to store categorized information. It's not 
necessarily ordered, and each value can be accessed through its key.

